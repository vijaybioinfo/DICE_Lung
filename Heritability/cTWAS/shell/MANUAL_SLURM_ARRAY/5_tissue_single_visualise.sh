disease="${disease##*/}"
study="${inputdir##*/}"
###############################################################
###############################################################
outpath="../../OUT/single/$disease/$study"
###############################################################
###############################################################
if [[ ! -d $outpath ]]; then
  mkdir -p "$outpath"
fi
####################################################
####################################################
####################################################
/usr/bin/time -v \
Rscript "../../scripts/6_summarise.R" \
  --inputdir "$inputdir" \
  --snp_map "/../../intermediary/snp_map/ukb_b38_0.1_chrom_all.rds" \
  --study "$study" \
  --gwas_n 343621 \
  --genome_version "b38" \
